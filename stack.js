// Stack

/**
 * Features of a Stack
 * It is a sequential collection of elements that follows the principle of Last In First Out (LIFO)
 * The last element inserted into the stack is first element to be removed
 * The best analogy for a stack is a stack of plate
 * Stack is an abstract data type. It is defined by its behaviour rather than being a mathematical model
 * So, a stack is simply a list that behave like a stack
 * It support two operations:
 * push to add an element to the end of a collection
 * pop to remove an element from the end of a collection
 */

